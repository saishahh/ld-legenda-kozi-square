      }
    },
    closeChat() {
      const chatWindow = document.getElementById('whatsapp-chat');
      chatWindow.classList.add('hide');
      chatWindow.classList.remove('show');
    },
    showChat() {
      const chatWindow = document.getElementById('whatsapp-chat');
      chatWindow.classList.add('show');
      chatWindow.classList.remove('hide');
    },
  },
  created() {
    const now = new Date();
